package com.e.provamobile;

import java.util.Date;

public class Equino {
    private String nome;
    private String nomeDono;
    private int matricula;
    private Date idade;

    public String getNome() { return nome; }

    public void setNome(String nome) { this.nome = nome; }

    public String getNomeDono() { return nomeDono;}

    public void setNomeDono(String nomeDono) { this.nomeDono = nomeDono;}

    public int getMatricula() { return matricula; }

    public void setMatricula(int matricula) { this.matricula = matricula; }

    public Date getIdade() { return idade; }

    public void setIdade(Date idade) { this.idade = idade;}
}
